import pyautogui
from time import sleep

def youtube():
    pyautogui.press("win")
    sleep(0.6)
    pyautogui.write("chrome")
    sleep(0.6)
    pyautogui.press("enter")
    sleep(0.6)
    pyautogui.write("youtube.com")
    sleep(0.6)
    pyautogui.press("enter")

